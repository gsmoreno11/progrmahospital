
package taller6;

public class iinstorial {
    private String codigo;
    public  String fecha;
    public  String idPaciente;
    public String idMedico; 
    public String observaciones;

    public iinstorial(String codigo, String fecha, String idPaciente, String idMedico, String observaciones) {
        this.codigo = codigo;
        this.fecha = fecha;
        this.idPaciente = idPaciente;
        this.idMedico = idMedico;
        this.observaciones = observaciones;
    }
    
}
